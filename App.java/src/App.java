public class App {
    public static void main(String[] args) throws Exception {
        
        int a = 4, b = 5;
        int c;
        c = a / b;
        System.out.println("c = " + c);
   
        double d;
        d = a / b;
        System.out.println("d = " + d);
   
        double e;
        e = (double) a / b; //Conversão deve ser explícita
        System.out.println("e = " + e);
   
    
    }
}
